public class Student4 implements Cloneable {  
public static void main(String[] args) {  
int number = 1;  
switch (number){  
case 2:  
System.out.println("number is 2");  
break;  
case 5:  
System.out.println("number is 5");  
break;  
default:  
System.out.println(number);  
}  
}  
}